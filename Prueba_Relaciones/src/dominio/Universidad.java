
package dominio;

public class Universidad {
   private Facultad facultad;
   private int nroFacultades;
   
   public Universidad(Facultad facultad, int nroFacultades){
       this.facultad= facultad;
       this.nroFacultades= nroFacultades;
   }
   public Facultad getCiencias_Ba(){
       return this.getCiencias_Ba();
   }
   public Facultad getCiencias_Ex(){
       return this.getCiencias_Ex();
   }
}
